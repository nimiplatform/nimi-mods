#!/usr/bin/env node

import { promises as fs } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import YAML from 'yaml';
import { getConfig } from './spec-kernel-config.mjs';
import { modSlugFromPath, normalizeWorkspaceEntry, resolveWorkspaceModDir } from './workspace-mods.mjs';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const modsRoot = path.resolve(scriptDir, '..');

function normalizeMarkdown(markdown) {
  return `${markdown.replace(/\n{3,}/gu, '\n\n').replace(/\n+$/u, '\n')}`;
}

function parseArgs(argv) {
  const args = {
    mod: '',
    check: false,
  };

  for (let i = 0; i < argv.length; i += 1) {
    const token = argv[i];
    if (token === '--mod') {
      args.mod = String(argv[i + 1] || '').trim();
      i += 1;
      continue;
    }
    if (token === '--check') {
      args.check = true;
    }
  }

  if (!args.mod) {
    throw new Error('missing --mod');
  }
  args.mod = normalizeWorkspaceEntry(args.mod);
  return args;
}

function renderDoc({ mod, title, sourceRelPath, parsed }) {
  return normalizeMarkdown([
    `# ${title}`,
    '',
    `> AUTO-GENERATED by \`scripts/generate-mod-spec-kernel-docs.mjs --mod ${mod}\`. DO NOT EDIT.`,
    `> Source: \`${sourceRelPath}\``,
    '',
    '```yaml',
    YAML.stringify(parsed, { lineWidth: 0 }).trimEnd(),
    '```',
    '',
  ].join('\n'));
}

function renderIndex({ mod, entries }) {
  let out = [
    '# Generated Kernel Views',
    '',
    `> AUTO-GENERATED by \`scripts/generate-mod-spec-kernel-docs.mjs --mod ${mod}\`. DO NOT EDIT.`,
    '',
    '| File | Source |',
    '|---|---|',
  ].join('\n');

  for (const entry of entries) {
    out += `\n| \`${entry.output}\` | \`tables/${entry.input}\` |`;
  }

  out += '\n';
  return normalizeMarkdown(out);
}

async function readYaml(filePath) {
  const raw = await fs.readFile(filePath, 'utf8');
  const parsed = YAML.parse(raw);
  if (!parsed || typeof parsed !== 'object') {
    throw new Error(`YAML root must be object: ${filePath}`);
  }
  return parsed;
}

async function main() {
  const { mod, check } = parseArgs(process.argv.slice(2));
  const modSlug = modSlugFromPath(mod);
  const config = getConfig(modSlug);

  const specRoot = resolveWorkspaceModDir(modsRoot, config.specRoot);
  const tablesDir = path.join(specRoot, 'kernel', 'tables');
  const generatedDir = path.join(specRoot, 'kernel', 'generated');
  await fs.mkdir(generatedDir, { recursive: true });

  const generatedEntries = [];
  for (const spec of config.tableSpecs) {
    const inputPath = path.join(tablesDir, spec.input);
    const outputPath = path.join(generatedDir, spec.output);
    const parsed = await readYaml(inputPath);
    const rendered = renderDoc({
      mod,
      title: spec.title,
      sourceRelPath: `${config.specRoot}/kernel/tables/${spec.input}`,
      parsed,
    });
    generatedEntries.push({ ...spec, outputPath, rendered });
  }

  const indexPath = path.join(generatedDir, 'index.md');
  const renderedIndex = renderIndex({ mod, entries: config.tableSpecs });

  if (check) {
    const drifted = [];
    for (const entry of generatedEntries) {
      let current = '';
      try {
        current = await fs.readFile(entry.outputPath, 'utf8');
      } catch {
        drifted.push(entry.outputPath);
        continue;
      }
      if (current !== entry.rendered) {
        drifted.push(entry.outputPath);
      }
    }

    let currentIndex = '';
    try {
      currentIndex = await fs.readFile(indexPath, 'utf8');
    } catch {
      drifted.push(indexPath);
    }

    if (currentIndex !== renderedIndex) {
      drifted.push(indexPath);
    }

    if (drifted.length > 0) {
      process.stderr.write(`[${mod}] kernel generated docs drift detected:\n`);
      for (const file of drifted) {
        process.stderr.write(`  - ${path.relative(modsRoot, file)}\n`);
      }
      process.stderr.write(`run \`pnpm -C nimi-mods run generate:spec:${modSlug}-kernel-docs\`\n`);
      process.exitCode = 1;
      return;
    }

    process.stdout.write(`[${mod}] kernel generated docs are up-to-date (${generatedEntries.length + 1} files)\n`);
    return;
  }

  for (const entry of generatedEntries) {
    await fs.writeFile(entry.outputPath, entry.rendered, 'utf8');
  }
  await fs.writeFile(indexPath, renderedIndex, 'utf8');
  process.stdout.write(`[${mod}] generated kernel docs (${generatedEntries.length + 1} files)\n`);
}

main().catch((error) => {
  process.stderr.write(`generate-mod-spec-kernel-docs failed: ${String(error)}\n`);
  process.exitCode = 1;
});
